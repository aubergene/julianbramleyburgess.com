			<p>
				Julian Bramley Burgess explores equilibriums and geometric principles in an experimental
				way. His work includes hanging mobile installations, mechanically and hand plotted prints
				and projections of digitally produced constructions.
			</p>

			<p>
				Currently he experiments with creating hanging whippletree structures formed from regular
				polygons. Finding equilibriums and balance is a key part to this work which look at the
				entangled behavour of how hierachies form complex relationships. He uses his background in
				software engineering to understand the principals and limits behind these relations
			</p>

			<p>
				His prints and drawings relate to his sculptural work. In the work Dreamspace he harvest
				over a hundred thousand dream diary entries. These were then fed into a high dimensional
				spacing using a machine learning algorithm. The resulting space was then reprojected and
				plotted in the familiar form of a stereographic celestial map, where the stars are the
				objects of dreams and the constellations the links between us all.
			</p>

